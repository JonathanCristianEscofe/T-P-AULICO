  private void armarCabecera() {

       tab.addColumn("Categoria");
        tab.addColumn("DNI");
        tab.addColumn("Apellido");
        tab.addColumn("Nombre");

        jTlistado.setModel(tab);

    }

    private void borrarFilas() {
        int f = jTlistado.getRowCount() - 1;
        for (; f >= 0; f--) {
            tab.removeRow(f);
        }
    }
