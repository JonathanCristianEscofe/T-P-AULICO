    private void jCempresasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCempresasActionPerformed
        // TODO add your handling code here:      
        borrarFilas();
                       

        for (Empresa empre : Empleador.lista) {
            if (empre.getRazonSocial().equals(jCempresas.getSelectedItem())) {
                for (Empleado e : empre.getEmpleados()) {
                    tab.addRow(new Object[]{
                        e.getCategoria(),
                        e.getDni(),
                        e.getNombre(),
                        e.getApellido()
                        });
                }
              
            }
        }
                                                

    }//GEN-LAST:event_jCempresasActionPerformed


